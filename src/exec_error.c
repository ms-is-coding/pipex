#include "pipex.h"
#include "libft.h"

inline int	pipex_pipe_error(char *name)
{
	ft_dprintf(2, "%s: Error: %m\n", name);
	return (1);
}

inline int	pipex_fork_error(char *name, int pipefd[2])
{
	ft_dprintf(2, "%s: Error: %m\n", name);
	close(pipefd[0]);
	close(pipefd[1]);
	return (1);
}
