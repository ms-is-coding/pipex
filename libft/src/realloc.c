#include "libft.h"
#include <stdlib.h>

void	*ft_realloc(void *ptr, size_t old_sz, size_t new_sz)
{
	void	*alloc;

	if (!ptr)
		return (malloc(new_sz));
	if (new_sz == 0)
	{
		free(ptr);
		return (NULL);
	}
	alloc = malloc(new_sz);
	if (!alloc)
		return (NULL);
	if (new_sz <= old_sz)
		ft_mempcpy(alloc, ptr, new_sz);
	else
		ft_mempcpy(alloc, ptr, old_sz);
	free(ptr);
	return (alloc);
}
